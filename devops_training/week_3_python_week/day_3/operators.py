#  Python allows us to change the default behavior of an operator depending on the operands that we use.
# This practice is referred to as "operator overloading".
num1 = 1
num2 = 2
print(num1+num2)

str1 = "Python"
str2 = "Programming"
print(str1+" "+str2)
# There isn't "+ int", "+ float", "+ char" etc, operators for each type.
